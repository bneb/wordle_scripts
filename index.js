function getFreq(words) {
  var freq = {};

  for (var i=0; i<words.length; i++) {
    var word = words[i]

    for (var j=0; j<word.length; j++) {
      var character = word[j];

      if (freq[character]) {
        freq[character]++;
      } else {
        freq[character] = 1;
      }
    }
  }

  return freq;
};

var freq = getFreq(words);

