function filterWarm(warm, word) {
  for (var w in warm) {
    var ix = word.indexOf(w);
    if (ix === -1 || warm[w].has(ix)) { return false; }
  }

  return true;
}

function filterWord(hot, warm, cold, word) {
  if (!filterWarm(warm, word)) { return false; }

  for (var i=0; i<word.length; i++) {
    l = word[i]
    if (cold.has(l)) { return false; }
    if (hot[i] && hot[i] != l) { return false; }
  }

  return true;
}

function score(word, freq) {
  var score = 0;

  for (let i=0; i<word.length; i++) { score += freq[word[i]]; }
  var numUnique = (new Set(word)).size

  return score * Math.sqrt(numUnique);
}

function getHot() {
  var hot = {};
  var els = document.getElementsByClassName('hot');

  Array.from(els).forEach(e => {
    var col = parseInt(e.id.substring(2,3))
    hot[col-1] = e.value;
  });

  return hot;
}

function getWarm() {
  var warm = new Map()
  var els = document.getElementsByClassName('warm');

  Array.from(els).forEach(e => {
    var col = parseInt(e.id.substring(2,3))
    var l = e.value;

    if (warm.has(l)) {
      warm[l].add(col-1);
    } else {
      warm[l] = new Set([col-1]);
    }
  });

  return warm;
}

function getCold(hot, warm) {
  var els = document.getElementsByClassName('cold');
  var cold = Array.from(els).map(e => e.value);

  cold = cold.filter(c => !warm[c] && c != '');
  cold = cold.filter(c => !Object.values(hot).includes(c));

  return new Set(cold);
}

function nextGuess() {
  var hot = getHot();
  var warm = getWarm();
  var cold = getCold(hot, warm);

  var filtered = filterWords(hot, warm, cold, words);
  var scored = [];

  filtered.forEach(w => scored.push([score(w, freq), w]));

  if (filtered.length == 0) {
    guess = 'none!';
  } else {
    scored.sort((a, b) => { return b[0]-a[0]; });
    guess = scored[0][1];
  }

  for (let i=0; i<guess.length; i++) {
    var l = guess[i];
    document.getElementById(`c6${i+1}`).value = l;
  }

  return guess
}

function nextInput(el) {
  // Using 1-based index.
  if (el.value.length < el.maxLength) {
    el.className = 'cold';
    return null;
  }

  if (el.id === "c55") { return null; }

  var row = parseInt(el.id.substring(1,2))
  var col = parseInt(el.id.substring(2,3))

  if (col === 5 && row < 6) { row++; }
  col = (col % 5) + 1
  
  document.getElementById(`c${row}${col}`).focus();
}

function toggleColor(el) {
  if (el.value.length < el.maxLength) { return null; }
  
  switch(el.className) {
    case 'warm':
      el.className = 'hot';
      break;
    case 'hot':
      el.className = 'cold';
      break;
    case 'cold':
      el.className = 'warm';
      break;
  }
}

function checkBackspace(event, el) {
  if (el.value.length > 0 || el.id === "c11") { return null; }
  if (event.key != "Backspace" && event.key != "Delete") { return null; }

  var row = parseInt(el.id.substring(1,2))
  var col = parseInt(el.id.substring(2,3))

  if (col === 1) {
    if (row > 1) { row--; }
    col = 5;
  } else {
    col--;
  }
  
  document.getElementById(`c${row}${col}`).focus();
}

