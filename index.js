Array.from(inputs).forEach((e) => {
  e.setAttribute('maxlength', 1);
  if (e.id.substring(1,2) === '6') {
    e.className = 'guess';
  } else {
    e.className = 'cold';
  }
  e.oninput = function() { nextInput(e); nextGuess(); };
  e.onclick = function() { toggleColor(e); nextGuess(); };
  e.onkeydown = function(event) { checkBackspace(event, e); nextGuess(); };
});

